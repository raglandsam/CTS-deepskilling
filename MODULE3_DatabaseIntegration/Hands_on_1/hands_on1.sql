create table DEPARTMENTS(
	department_id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	dept_name VARCHAR(100) NOT NULL,
	hod_name VARCHAR(100),
	budget DECIMAL(12,2));
	
create table STUDENTS(
	student_id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	first_name VARCHAR(50) NOT NULL,
	last_name VARCHAR(50) NOT NULL,
	email varchar(50) UNIQUE NOT NULL,
	date_of_birth DATE,
	department_id INT,
	enrollment_year INT,
	
	Constraint fk_student_dept foreign key(department_id) references departments(department_id) on delete set null);

create table courses(
	course_id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	course_name varchar(50) not null,
	course_code varchar(20) unique,
	credits int,
	department_id int,
	constraint dept_course foreign key(department_id) references departments(department_id) on delete set null);
	
create table enrollments (
	enrollment_id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	student_id INT,
	course_id INT,
	enrollment_date DATE,
	grade CHAR(2) check ( grade in ('A','B','C','D','E','F')),
	constraint enrollment_stud foreign key(student_id) references students(student_id) on delete set null,
	constraint course_enrollments foreign key(course_id) references courses(course_id) on delete set null);

create table professors (
	professor_id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	prof_name Varchar(100) not null,
	email varchar(100) unique,
	department_id int,
	salary decimal(10,2),
	constraint prof_dept foreign key(department_id) references departments(department_id) on delete set null);

