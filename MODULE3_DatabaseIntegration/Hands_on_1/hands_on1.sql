-------- TASK 2 --------- : ARCHITECTURAL NORMALIZATION AUDIT

-- 1. FIRST NORMAL FORM (1NF) COMPLIANCE:
-- Definition: All attributes must hold atomic values; repeating groups/arrays are barred.
-- Audit Analysis: Inside the 'STUDENTS' and 'DEPARTMENTS' tables, fields such as 'email', 
-- 'first_name', and 'dept_name' contain scalar, indivisible data types. If an un-normalized 
-- field like 'phone_numbers VARCHAR' existed holding comma-separated values ('9998887776, 9993332221'), 
-- it would violate 1NF, breaking index traversal. This schema satisfies 1NF.

-- 2. SECOND NORMAL FORM (2NF) COMPLIANCE:
-- Definition: Must satisfy 1NF, and all non-key columns must possess Full Functional 
-- Dependency on the complete Primary Key (No Partial Dependencies on composite keys).
-- Audit Analysis: The junction table 'ENROLLMENTS' uses an auto-incrementing identity key 
-- ('enrollment_id') as its PK. The alternate composite candidate key is (student_id, course_id). 
-- The non-key attributes 'enrollment_date' and 'grade' depend explicitly on the *combination* -- of who the student is AND which course they are taking. There are no attributes (like 
-- 'course_duration') that depend only on a subset ('course_id') of the candidate key. 
-- This schema satisfies 2NF.

-- 3. THIRD NORMAL FORM (3NF) COMPLIANCE:
-- Definition: Must satisfy 2NF, and no non-key attribute can possess a transitive 
-- dependency on the Primary Key through another non-key attribute (No Non-key -> Non-key dependencies).
-- Audit Analysis: Let's analyze the dependency chain:
--   student_id (PK) -> department_id (Foreign Key)
--   department_id   -> dept_name / hod_name
-- If 'dept_name' or 'hod_name' were stored directly inside the 'STUDENTS' table, a transitive 
-- dependency would form: student_id -> department_id -> dept_name. This would cause data 
-- mutation anomalies during department re-organizations. By isolating department details into 
-- the 'DEPARTMENTS' table and referencing only 'department_id', transitive dependencies are eliminated. 
-- This schema satisfies 3NF.



