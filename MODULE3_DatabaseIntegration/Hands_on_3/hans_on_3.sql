create table department_transfer_log (
	log_id INT generated by default as identity primary key,
	student_id INT,
	old_department_id INT,
	new_department_id INT,
	transfer_time timestamp default current_timestamp);
	
create or replace function transfer_student_dept(
	p_student_id INT,
	p_new_department_id INT)
	returns text as $$
	declare 
		d_old_department_id INT;
	begin 
		select department_id into d_old_department_id from students where student_id=p_student_id;
		--CHECK IF OLD DEPT EXISTS
		if d_old_department_id is null then 
			Raise exception 'ERROR: student not found';
		end if;
		--UPDATE IN STUDENT TABLE

		update students set department_id=p_new_department_id where student_id=p_student_id;
		--INSERT LOG
		INSERT INTO department_transfer_log (student_id,old_department_id, new_department_id)
		values (p_student_id,d_old_department_id, p_new_department_id);
		
		RAISE NOTICE 'Transfer sequence finalized successfully.';
		
		return 'success:transfer verified';
		
		EXCEPTION 
    	WHEN OTHERS THEN
        	-- Catches any parsing/constraint failure, rolls back the session mutations entirely, and signals the host
        	RAISE NOTICE 'CRITICAL FAULT: Transfer failed. Rolling back changes.';
        	RAISE;
		End;
$$ language plpgsql;

