create or replace function fn_enroll_student(
	p_student_id INT,
	p_course_id int,
	p_enrollment_date date)
returns text as $$
declare 
	v_exists int;
begin
	--validation 1
	if not exists(select 1 from students where student_id=p_student_id)
		then 
			raise exception 'ERROR: STUDENT ID % DOES NOT EXISTS',p_student_id;
	end if;

	select count(*) into v_exists
	from enrollments where student_id=p_student_id and course_id=p_course_id;
	
	--validation 2
	if v_exists>0 then 
	raise exception 'Error: already enrolled';
	end if;
	
	insert into enrollments (student_id, course_id, enrollment_date)
	values (p_student_id, p_course_id, p_enrollment_date);
	return 'succesfully enrolled';
	

end;
$$ language plpgsql;

